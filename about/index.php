      		<h2>History of Kapol Samaj of Southern California</h2>
            <h3>The Birth of Kapol and Kapol Samaj of America</h3><br />

			<p>The history of how the Kapol caste originated is really fascinating. According to an ancient Sanskrit treatise called the 'Kundal Puran', King Mandhak conquered a major portion of North India. He then commenced pilgrimage. He visited His Holiness Sri Kanva Rishi who resided in the Papanod Ashram (Monastery) located near a town called Prabhas Patan in Saurashtra. It was a famous place of worship and was densely populated by Brahmins, Vaishyas and other communities. Due to the paucity of land and other natural resources, conflicts between different groups were common in this town. In order to resolve this recurring problem, King Mandhak planned to build a new town.</p>

 <p>It is customary to perform a religious ceremony called Bhoomi-Pujan (Ground Breaking) before a house is built. As when a whole new town is to be built, King Mandhak was advised to perform a Maha-Yagna. King Mandhak sought the blessings and assistance of Sri Kanva Rishi to conduct the proposed Maha-Yagna. H.H. Sri Kanva Rishi asked Gallav Rishi, one of the 18 Rishis who resided in his monastery to take this responsibility with the help of other Rishis. They made a lot of preparations and Brahmins from all over India such as Gupta Prayag, Kashi, Badrikedar and other places were invited. The Brahmins and Vaishyas of Prabhas Patan were also persuaded to participate in the Maha-Yagna.   It was a big success. King Mandhak and H. H. Sri Kanva Rishi were extremely pleased to see the quarreling groups of Brahmins and Vaishyas interacting peacefully with each other.</p>

<p>They offered Sri Gallav Rishi a reward in recognition of his achievement. Sri Gallav Rishi prayed:  'You’re Honor, due to your blessings, a number of Brahmins and thousands of Vaishyas have come to this new town to participate in the Mahan Yang. Please officially inaugurate this town at this most auspicious moment for their stay. If you are satisfied with my work and if your Honor wishes to reward me, I request that out of 36,000 Vaishyas who have assembled here, 6,000 Vaishyas who are wearing big earrings stretching up to their cheeks (Kapol, in Sanskrit), be known by my name'.  So initially these 6,000 Vaishyas were called Gallavas - named after Gallava Rishi. Subsequently they were called Kapols.</p>

<p>A handful of Kapols of SoCal. came together in August of 2003 to form a body with the objective to be able to meet, get acquainted and have some nirmal ananad with the fellow Kapols. Behind this motive, was an obvious motive of telling our next generation of who we are, what we are and what it means to us. And so Kapol Samaj of America (West Coast Chapter) was born, with its very first Sammelan on February 28, 2004, with an incredible success! From then on this Samaj has had several programs. Though it has limited resources both in terms of human and financial, it plans to focus mainly on youth, seniors and matrimonial.</p>

<p>Samaj’s working committee (members have no titles) are: Nila Mody, Jagruti Vora, Khyati Jhaveri, Bhavesh Muni, Pankaj Mehta, Sandip Mody, Deepak Parekh, Sheetal Mehta, Neeta Vora, Meera Mody, and Vianod Mehta. They all work like a family, in support and harmony.</p>
      
